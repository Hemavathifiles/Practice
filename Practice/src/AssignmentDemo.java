public class AssignmentDemo {
    public static void main(String[] args) {
        int a = 10;

        // Add and assign
        a += 5;  // a = a + 5
        System.out.println("a after +=: " + a);

        // Subtract and assign
        a -= 3;  // a = a - 3
        System.out.println("a after -=: " + a);

        // Multiply and assign
        a *= 2;  // a = a * 2
        System.out.println("a after *=: " + a);

        // Divide and assign
        a /= 4;  // a = a / 4
        System.out.println("a after /=: " + a);

        // Modulus and assign
        a %= 3;  // a = a % 3
        System.out.println("a after %=: " + a);
    }
}
