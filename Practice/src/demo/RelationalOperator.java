package demo;

public class RelationalOperator {

	public static void main(String[] args) {
		int num1=2;
		int num2=3;
		System.out.println("num1>num2"  +"-"+ (num1>num2));
		System.out.println("num1<num2"  +"-"+ (num1<num2));
		System.out.println("num1>=num2" +"-"+  (num1>=num2));
		System.out.println("num1<=num2" +"-"+  (num1<=num2));
		System.out.println("num1==num2" +"-"+  (num1==num2));
		System.out.println("num1!=num2" +"-"+ (num1!=num2));
		
	}

}
